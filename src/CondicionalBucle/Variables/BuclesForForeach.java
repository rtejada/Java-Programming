package CondicionalBucle.Variables;

public class BuclesForForeach {

    public static void main (String arg[]){

        for (int i=0; i<2; i++){
                System.out.println("Roxana");
        }
        //Decremeto
        for (int i=2; i>0; i--){
            System.out.println("Roxana");
        }


        for (int i=0; i<10; i+=2){
            System.out.println("Roxana");
        }



    }
}
